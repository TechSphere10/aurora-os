void gfx_draw_pixel(int x, int y, uint8_t color) {
    if (x >= 0 && x < 320 && y >= 0 && y < 200) {
        uint8_t* screen = (uint8_t*)VIDEO_MEMORY;
        screen[y * 320 + x] = color;
    }
}

void gfx_clear_screen(uint8_t color) {
    uint8_t* screen = (uint8_t*)VIDEO_MEMORY;
    for (int i = 0; i < 320 * 200; i++) screen[i] = color;
}

void gfx_draw_char(int x, int y, char c, uint8_t color) {
    if (c < 32 || c > 95) c = 32; // Map unsupported to space
    c -= 32;
    for (int i = 0; i < 8; i++) {
        uint8_t row = font8x8_basic[(int)c][i];
        for (int j = 0; j < 8; j++) {
            if (row & (1 << (7-j))) gfx_draw_pixel(x + j, y + i, color);
        }
    }
}

void gfx_init() {
    gfx_clear_screen(1); // Blue background
    
    // Draw Splash Screen Logo
    const char* logo = "AURORA OS";
    int x = (320 - (kstrlen(logo) * 8)) / 2;
    int y = 90;
    for (int i = 0; logo[i]; i++) {
        gfx_draw_char(x + (i * 8), y, logo[i], 15); // White text
    }
}